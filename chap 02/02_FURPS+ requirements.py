# One commonly used model for classifying software quality attributes is the acronym FURPS (functionality, usability, reliability, performance, supportability)
# functionality : It is The core of what the customer wants (Capability, Reusability, Security)
# Usability affects the person who will be using the program (Human Factors, Aesthetics, Consistency, Documentation)
# Reliability : (Availability, Failure Rate and Duration, Predictability)
# Performance : (Speed, Efficiency, Resource Consumption, Scalability)
# Supportability : (Testablity, Extensibilitiy, Serviceability, Configurability)
# FURPS+ :(Desgin, Implementation, Interface, Physical)
